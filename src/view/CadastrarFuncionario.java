    int idAlteracao=0;
    public CadastrarFuncionario(int idAlteracao) {
        initComponents();
        this.idAlteracao = idAlteracao;
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/restaurante","root","root");
            String sql = "select * from pessoa where idPessoa = ? ";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setInt(1, idAlteracao);
            ResultSet rs = stmt.executeQuery();
            while(rs.next()){
                txtID.setText(rs.getString("idPessoa"));
                txtID.setEnabled(false);
                txtNome.setText(rs.getString("nome"));
                txtTelefone.setText(rs.getString("telefone"));
                txtCPF.setText(rs.getString("cpf"));
                txtCPF.setEnabled(false);
                txtNumCarteira.setText(rs.getString("numCarteira"));
                txtNumCarteira.setEnabled(false);
                txtNumTrabalhos.setText(rs.getString("numTrabalhos"));
            }
            stmt.close();
            con.close();            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

