//O botão de salvar para tanto o novo funcente, quanto atualizar o contato.
    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String cpf = txtCPF.getText();
        String telefone = txtTelefone.getText();
        int numCarteira = Integer.parseInt(txtNumCarteira.getText());
        Funcionario func = new Funcionario(numCarteira, nome, cpf, telefone);
        func.setNumTrabalhos(Integer.parseInt(txtNumTrabalhos.getText()));
        CadastroFuncionarios cadFunc = new CadastroFuncionarios();
        if(idAlteracao == 0) {
            cadFunc.adicionar(func);
            JOptionPane.showMessageDialog(this, "Funcionário Cadastrado com Sucesso!");
            this.dispose();
        } else {
            try {
                cadFunc.atualizar(func);
                JOptionPane.showMessageDialog(this, "Funcionário Atualizado com Sucesso!");
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(CadastrarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btSalvarActionPerformed

